/**
 * Priority country codes to show at the top of country pickers
 * Order: United States, United Kingdom, Germany, Canada, India, France, Spain
 */
export const PRIORITY_COUNTRY_CODES = [
  "US",
  "GB",
  "DE",
  "CA",
  "IN",
  "FR",
  "ES",
];

/**
 * Sorts countries with priority countries first, then alphabetically by name.
 * When a search term is provided, countries starting with the search term are prioritized.
 */
export function sortCountriesWithPriority<
  T extends { name: string; code: string },
>(countryList: T[], searchTerm: string = ""): T[] {
  const term = searchTerm.toLowerCase();

  return [...countryList].sort((a, b) => {
    const aIsPriority = PRIORITY_COUNTRY_CODES.includes(a.code);
    const bIsPriority = PRIORITY_COUNTRY_CODES.includes(b.code);

    // If searching, prioritize matches that start with the search term
    if (term) {
      const aStartsWith = a.name.toLowerCase().startsWith(term);
      const bStartsWith = b.name.toLowerCase().startsWith(term);
      if (aStartsWith && !bStartsWith) return -1;
      if (!aStartsWith && bStartsWith) return 1;
    }

    // Priority countries come first
    if (aIsPriority && !bIsPriority) return -1;
    if (!aIsPriority && bIsPriority) return 1;

    // Within priority countries, maintain the defined order
    if (aIsPriority && bIsPriority) {
      return (
        PRIORITY_COUNTRY_CODES.indexOf(a.code) -
        PRIORITY_COUNTRY_CODES.indexOf(b.code)
      );
    }

    // Non-priority countries sorted alphabetically
    return a.name.localeCompare(b.name);
  });
}
